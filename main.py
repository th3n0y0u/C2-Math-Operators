# Math
x = 7
y = 2

# Output : x + y = 9
print("x + y = " + str(x + y))
# Output : x - y = 9
print("x - y = " + str(x - y))
# Output : x * y = 14
print("x * y = " + str(x * y))
# Output: x / y = 3.5
print("x / y = " + str(x / y))
# Output: x // y = 3
print("x // y = " + str(x // y))
# Output: x ** y = 49
print("x ** y = " + str(x ** y))

